#!/usr/bin/env python3
"""Convert APRIL materialized repo into infilling training examples.

This script takes a materialized Lean repository (created by materialize-repo.sh)
and generates infilling training examples by replacing each tactic with <HOLE>.
For each theorem with N tactics, it creates N training examples where each example
has one tactic replaced by <HOLE> and the original tactic as the target.

Output format matches the LeanDojo theorem JSON structure with additional fields
for infilling tasks.
"""

from __future__ import annotations

import argparse
import json
import os
import random
import re
import signal
import subprocess
from collections import Counter
from concurrent.futures import ThreadPoolExecutor, as_completed
from pathlib import Path
from typing import Any

from tqdm.auto import tqdm

DEFAULT_URL = "https://huggingface.co/datasets/uw-math-ai/APRIL"
APRIL_DIR = Path(__file__).resolve().parent
REPO_ROOT = APRIL_DIR.parents[1]
EXTRACT_DATA_PATH = (
    REPO_ROOT / "lean_dojo_v2" / "lean_dojo" / "data_extraction" / "ExtractData.lean"
)
DEFAULT_PROJECT_PATH = APRIL_DIR / "april_eval_project"
# APRIL has pre-split train/val manifests in materialized/{train,val}/
DEFAULT_TRAIN_MANIFEST_PATH = APRIL_DIR / "materialized" / "train" / "thme_train.manifest.jsonl"
DEFAULT_VAL_MANIFEST_PATH = APRIL_DIR / "materialized" / "val" / "thme_val.manifest.jsonl"
DEFAULT_OUTPUT_JSON = APRIL_DIR / "leandojo_infilling" / "train.json"
HOLE_TOKEN = "<HOLE>"


def configure_elan_toolchain(project_path: Path) -> None:
    toolchain_file = project_path / "lean-toolchain"
    if not toolchain_file.is_file():
        return

    toolchain = toolchain_file.read_text(encoding="utf-8").strip()
    if not toolchain:
        return

    os.environ["ELAN_TOOLCHAIN"] = toolchain


def read_jsonl(path: Path) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    with path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            rows.append(json.loads(line))
    return rows


def load_manifest(manifest_path: Path) -> list[dict[str, Any]]:
    """Load the manifest file that maps row indices to materialized file paths."""
    if not manifest_path.is_file():
        raise FileNotFoundError(f"Manifest file not found: {manifest_path}")
    return read_jsonl(manifest_path)


def infer_full_name(code: str, default_name: str) -> str:
    """Extract theorem name from code or use default."""
    m = re.search(r"\b(?:theorem|lemma)\s+([A-Za-z0-9_'.]+)", code)
    if m:
        return m.group(1)
    return default_name


def infer_statement(code: str) -> str:
    """Extract the theorem statement (between theorem name and := by)."""
    m = re.search(
        r"\b(?:theorem|lemma)\s+[A-Za-z0-9_'.]+\s*(.*?)\s*:=\s*by",
        code,
        flags=re.DOTALL,
    )
    if m:
        return re.sub(r"\s+", " ", m.group(1)).strip()
    return ""


def run_extract_data(
    project_path: Path,
    lean_relative_path: Path,
    timeout_seconds: int,
) -> tuple[int, str, str]:
    """Run ExtractData.lean on a file to get tactic traces."""
    cmd = [
        "lake",
        "env",
        "lean",
        "--run",
        str(EXTRACT_DATA_PATH),
        str(lean_relative_path),
    ]
    env = os.environ.copy()
    prev_lean_path = env.get("LEAN_PATH", "")
    env["LEAN_PATH"] = (
        f"{project_path}:{prev_lean_path}" if prev_lean_path else str(project_path)
    )

    proc = subprocess.Popen(
        cmd,
        cwd=project_path,
        env=env,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        text=True,
        start_new_session=True,
    )
    try:
        stdout, stderr = proc.communicate(timeout=timeout_seconds)
    except subprocess.TimeoutExpired as ex:
        if proc.poll() is None:
            try:
                os.killpg(proc.pid, signal.SIGKILL)
            except ProcessLookupError:
                pass
        stdout, stderr = proc.communicate()
        ex.stdout = stdout
        ex.stderr = stderr
        raise
    return proc.returncode, stdout, stderr


def get_ast_json_path(project_path: Path, lean_relative_path: Path) -> Path | None:
    """Find the AST JSON file generated by ExtractData.lean."""
    rel = lean_relative_path.with_suffix(".ast.json")
    candidates = [
        project_path / ".lake" / "build" / "ir" / rel,
        project_path / "build" / "ir" / rel,
    ]
    for p in candidates:
        if p.is_file():
            return p
    return None


def extract_raw_tactic(code_bytes: bytes, start: int, end: int) -> str:
    """Extract tactic text from byte positions in the source code."""
    if start < 0 or end < start:
        return ""
    if start >= len(code_bytes):
        return ""
    end = min(end, len(code_bytes))
    return code_bytes[start:end].decode("utf-8", errors="ignore").strip()


def parse_byte_offset(value: Any) -> int | None:
    """Parse byte offset from AST JSON (handles int or dict formats)."""
    if isinstance(value, int):
        return value
    if isinstance(value, dict):
        byte_idx = value.get("byteIdx")
        if isinstance(byte_idx, int):
            return byte_idx
    return None


def parse_tactics_from_ast(ast_json_path: Path, code: str) -> list[dict[str, Any]]:
    """Parse tactics from AST JSON, returning detailed position information."""
    payload = json.loads(ast_json_path.read_text(encoding="utf-8"))
    raw_tactics = payload.get("tactics", [])
    code_bytes = code.encode("utf-8")

    tactics: list[dict[str, Any]] = []
    seen: set[tuple[str, str, str]] = set()

    for tac in raw_tactics:
        if not isinstance(tac, dict):
            continue

        start = parse_byte_offset(tac.get("pos"))
        end = parse_byte_offset(tac.get("endPos"))
        if start is None or end is None:
            continue

        tactic_text = extract_raw_tactic(code_bytes, start, end)
        state_before = str(tac.get("stateBefore") or "").strip()
        state_after = str(tac.get("stateAfter") or "").strip()

        # Skip invalid or unwanted tactics
        if not tactic_text or tactic_text == "sorry":
            continue
        if state_before == "no goals" or "·" in tactic_text:
            continue

        # Deduplicate by (state_before, tactic, state_after) signature
        sig = (state_before, tactic_text, state_after)
        if sig in seen:
            continue
        seen.add(sig)

        tactics.append({
            "tactic": tactic_text,
            "annotated_tactic": [tactic_text, []],
            "state_before": state_before,
            "state_after": state_after,
            "start_byte": start,
            "end_byte": end,
        })

    return tactics


def create_proof_with_hole(code: str, tactic_info: dict[str, Any], hole_token: str) -> str:
    """Replace a tactic at specific byte positions with the hole token."""
    code_bytes = code.encode("utf-8")
    start = tactic_info["start_byte"]
    end = tactic_info["end_byte"]

    before = code_bytes[:start].decode("utf-8", errors="ignore")
    after = code_bytes[end:].decode("utf-8", errors="ignore")

    return before + hole_token + after


def create_infilling_examples(
    theorem_record: dict[str, Any],
    tactics: list[dict[str, Any]],
    code: str,
    hole_token: str,
) -> list[dict[str, Any]]:
    """Create infilling examples for each tactic in a theorem.

    For each tactic, creates an example where that tactic is replaced by the hole token.
    """
    examples = []

    for i, tactic_info in enumerate(tactics):
        # Create proof with this tactic replaced by hole
        proof_with_hole = create_proof_with_hole(code, tactic_info, hole_token)

        example = {
            # Original LeanDojo fields
            "url": theorem_record["url"],
            "commit": theorem_record["commit"],
            "file_path": theorem_record["file_path"],
            "full_name": theorem_record["full_name"],
            "theorem_statement": theorem_record["theorem_statement"],
            "start": theorem_record["start"],
            "end": theorem_record["end"],
            # Infilling-specific fields
            "infilling": {
                "hole_index": i,
                "total_tactics": len(tactics),
                "proof_with_hole": proof_with_hole,
                "target_tactic": tactic_info["tactic"],
                "state_before_hole": tactic_info["state_before"],
                "state_after_hole": tactic_info["state_after"],
            },
            # Original traced tactics for reference
            "traced_tactics": tactics,
        }
        examples.append(example)

    return examples


def parse_tactics_from_raw_text(code: str) -> list[dict[str, Any]]:
    """Parse tactics using a comment-aware raw-text heuristic.

    This mode scans lines after `:= by`, strips comments, and treats each
    non-empty code line as one tactic candidate.
    """
    theorem_match = re.search(r"\b(?:theorem|lemma)\b\s+[A-Za-z0-9_'.]+", code)
    if theorem_match is None:
        return []

    by_match = re.search(r":=\s*by\b", code[theorem_match.end():])
    if by_match is None:
        return []

    proof_start = theorem_match.end() + by_match.end()

    # Build character to byte offset mapping for UTF-8
    char_to_byte = [0]
    for ch in code:
        char_to_byte.append(char_to_byte[-1] + len(ch.encode("utf-8")))

    tactics: list[dict[str, Any]] = []
    seen_spans: set[tuple[int, int]] = set()

    block_comment_depth = 0
    in_string = False
    escaped = False
    line_first_non_ws_code: int | None = None
    line_last_non_ws_code: int | None = None

    def flush_line() -> None:
        nonlocal line_first_non_ws_code, line_last_non_ws_code
        if line_first_non_ws_code is None or line_last_non_ws_code is None:
            line_first_non_ws_code = None
            line_last_non_ws_code = None
            return

        start_char = line_first_non_ws_code
        end_char_exclusive = line_last_non_ws_code + 1
        line_first_non_ws_code = None
        line_last_non_ws_code = None

        tactic_text = code[start_char:end_char_exclusive].strip()
        if not tactic_text or tactic_text == "sorry":
            return
        if "·" in tactic_text:
            return

        sig = (start_char, end_char_exclusive)
        if sig in seen_spans:
            return
        seen_spans.add(sig)

        tactics.append({
            "tactic": tactic_text,
            "annotated_tactic": [tactic_text, []],
            "state_before": "",
            "state_after": "",
            "start_byte": char_to_byte[start_char],
            "end_byte": char_to_byte[end_char_exclusive],
        })

    i = proof_start
    n = len(code)
    while i < n:
        ch = code[i]
        nxt = code[i + 1] if i + 1 < n else ""

        if ch == "\n":
            flush_line()
            i += 1
            continue

        if block_comment_depth > 0:
            if ch == "/" and nxt == "-":
                block_comment_depth += 1
                i += 2
                continue
            if ch == "-" and nxt == "/":
                block_comment_depth -= 1
                i += 2
                continue
            i += 1
            continue

        if in_string:
            if not ch.isspace():
                if line_first_non_ws_code is None:
                    line_first_non_ws_code = i
                line_last_non_ws_code = i
            if ch == '"' and not escaped:
                in_string = False
                escaped = False
            elif ch == "\\" and not escaped:
                escaped = True
            else:
                escaped = False
            i += 1
            continue

        if ch == "-" and nxt == "-":
            new_line_idx = code.find("\n", i)
            if new_line_idx == -1:
                i = n
            else:
                i = new_line_idx
            continue

        if ch == "/" and nxt == "-":
            block_comment_depth = 1
            i += 2
            continue

        if ch == '"':
            in_string = True
            escaped = False
            if line_first_non_ws_code is None:
                line_first_non_ws_code = i
            line_last_non_ws_code = i
            i += 1
            continue

        if not ch.isspace():
            if line_first_non_ws_code is None:
                line_first_non_ws_code = i
            line_last_non_ws_code = i

        i += 1

    flush_line()
    return tactics


def process_file(
    manifest_entry: dict[str, Any],
    project_path: Path,
    extract_timeout: int,
    hole_token: str,
    parser: str,
) -> tuple[int, list[dict[str, Any]] | None, dict[str, Any] | None]:
    """Process a single materialized file and return infilling examples or error info."""
    row_idx = manifest_entry["row_idx"]
    relative_path = Path(manifest_entry["relative_path"])
    file_path = project_path / relative_path
    module_name = manifest_entry["module"]
    uuid = manifest_entry.get("uuid", f"row_{row_idx}")

    # Read the Lean file
    if not file_path.is_file():
        return (
            row_idx,
            None,
            {
                "row_idx": row_idx,
                "reason": "file_not_found",
                "uuid": uuid,
                "module": module_name,
                "file_path": str(file_path),
            },
        )

    try:
        code = file_path.read_text(encoding="utf-8")
    except Exception as ex:
        return (
            row_idx,
            None,
            {
                "row_idx": row_idx,
                "reason": f"read_error:{type(ex).__name__}",
                "uuid": uuid,
                "module": module_name,
                "detail": str(ex),
            },
        )

    if parser == "raw":
        try:
            tactics = parse_tactics_from_raw_text(code)
        except Exception as ex:
            return (
                row_idx,
                None,
                {
                    "row_idx": row_idx,
                    "reason": f"parse_raw_text_exception:{type(ex).__name__}",
                    "uuid": uuid,
                    "module": module_name,
                    "detail": str(ex),
                },
            )
    else:
        # Run ExtractData to get tactic information
        try:
            returncode, stdout, stderr = run_extract_data(
                project_path,
                relative_path,
                timeout_seconds=extract_timeout,
            )
        except subprocess.TimeoutExpired as ex:
            timeout_stdout = (ex.stdout or "").strip()
            timeout_stderr = (ex.stderr or "").strip()
            detail_parts: list[str] = [str(ex)]
            if timeout_stderr:
                detail_parts.append(f"stderr:\n{timeout_stderr}")
            if timeout_stdout:
                detail_parts.append(f"stdout:\n{timeout_stdout}")
            detail = "\n\n".join(detail_parts)[:2000]
            return (
                row_idx,
                None,
                {
                    "row_idx": row_idx,
                    "reason": "extractdata_timeout",
                    "uuid": uuid,
                    "module": module_name,
                    "detail": detail,
                },
            )
        except Exception as ex:
            return (
                row_idx,
                None,
                {
                    "row_idx": row_idx,
                    "reason": f"extractdata_exception:{type(ex).__name__}",
                    "uuid": uuid,
                    "module": module_name,
                    "detail": str(ex),
                },
            )

        if returncode != 0:
            stderr = (stderr or "").strip()
            stdout = (stdout or "").strip()
            detail_parts: list[str] = []
            if stderr:
                detail_parts.append(f"stderr:\n{stderr}")
            if stdout:
                detail_parts.append(f"stdout:\n{stdout}")
            detail = "\n\n".join(detail_parts)[:2000]
            rec: dict[str, Any] = {
                "row_idx": row_idx,
                "reason": "extractdata_process_error",
                "uuid": uuid,
                "module": module_name,
            }
            if detail:
                rec["detail"] = detail
            return row_idx, None, rec

        # Find and parse the AST JSON
        ast_json_path = get_ast_json_path(project_path, relative_path)
        if ast_json_path is None:
            return (
                row_idx,
                None,
                {
                    "row_idx": row_idx,
                    "reason": "missing_ast_json",
                    "uuid": uuid,
                    "module": module_name,
                    "detail": str(relative_path),
                },
            )

        try:
            tactics = parse_tactics_from_ast(ast_json_path, code)
        except Exception as ex:
            return (
                row_idx,
                None,
                {
                    "row_idx": row_idx,
                    "reason": f"parse_ast_json_exception:{type(ex).__name__}",
                    "uuid": uuid,
                    "module": module_name,
                    "detail": str(ex),
                },
            )

    if not tactics:
        return (
            row_idx,
            None,
            {
                "row_idx": row_idx,
                "reason": "no_traced_tactics",
                "uuid": uuid,
                "module": module_name,
            },
        )

    # Create the base theorem record
    theorem_name = infer_full_name(code, module_name.replace(".", "_"))
    theorem_statement = infer_statement(code)
    source = str(manifest_entry.get("source") or "april")

    base_record = {
        "url": DEFAULT_URL,
        "commit": "",
        "file_path": str(relative_path),
        "full_name": theorem_name,
        "theorem_statement": theorem_statement,
        "start": [1, 1],
        "end": [1, 1],
    }

    # Create infilling examples
    infilling_examples = create_infilling_examples(
        base_record, tactics, code, hole_token
    )

    return row_idx, infilling_examples, None


def split_examples_train_val(
    examples: list[dict[str, Any]],
    val_ratio: float,
    seed: int,
) -> tuple[list[dict[str, Any]], list[dict[str, Any]]]:
    """Split examples into train/val at theorem level."""
    if val_ratio <= 0:
        return examples, []
    if val_ratio >= 1:
        return [], examples

    groups: dict[tuple[str, str], list[dict[str, Any]]] = {}
    for ex in examples:
        key = (str(ex.get("file_path", "")), str(ex.get("full_name", "")))
        groups.setdefault(key, []).append(ex)

    theorem_keys = list(groups.keys())
    rng = random.Random(seed)
    rng.shuffle(theorem_keys)

    num_theorems = len(theorem_keys)
    num_val_theorems = int(num_theorems * val_ratio)
    if num_val_theorems == 0 and num_theorems > 1:
        num_val_theorems = 1
    if num_val_theorems >= num_theorems and num_theorems > 1:
        num_val_theorems = num_theorems - 1

    val_key_set = set(theorem_keys[:num_val_theorems])
    train_examples: list[dict[str, Any]] = []
    val_examples: list[dict[str, Any]] = []
    for ex in examples:
        key = (str(ex.get("file_path", "")), str(ex.get("full_name", "")))
        if key in val_key_set:
            val_examples.append(ex)
        else:
            train_examples.append(ex)
    return train_examples, val_examples


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--project-path",
        type=Path,
        default=DEFAULT_PROJECT_PATH,
        help=(
            "Path to the materialized Lean project "
            f"(default: {DEFAULT_PROJECT_PATH})"
        ),
    )
    parser.add_argument(
        "--manifest-path",
        "--train-manifest-path",
        type=Path,
        default=DEFAULT_TRAIN_MANIFEST_PATH,
        dest="train_manifest_path",
        help=(
            "Path to the train manifest JSONL file that maps row indices to file paths "
            f"(default: {DEFAULT_TRAIN_MANIFEST_PATH})"
        ),
    )
    parser.add_argument(
        "--val-manifest-path",
        type=Path,
        default=None,
        help=(
            "Optional path to a separate validation manifest JSONL. "
            "If provided, creates infilling datasets for both train and val. "
            f"(default: {DEFAULT_VAL_MANIFEST_PATH})"
        ),
    )
    parser.add_argument(
        "--output-json",
        type=Path,
        default=DEFAULT_OUTPUT_JSON,
        help=(
            "Base output JSON path. If --val-ratio > 0, writes "
            "<stem>.train<suffix> and <stem>.val<suffix>; otherwise writes "
            "the full dataset to this path "
            f"(default: {DEFAULT_OUTPUT_JSON})"
        ),
    )
    parser.add_argument(
        "--max-theorems",
        type=int,
        default=None,
        help="Maximum number of theorems to process (default: all)",
    )
    parser.add_argument(
        "--max-examples-per-theorem",
        type=int,
        default=None,
        help="Maximum number of infilling examples per theorem (default: all tactics)",
    )
    parser.add_argument(
        "--extract-timeout",
        type=int,
        default=300,
        help="Per-file timeout (seconds) for tactic extraction (default: 300)",
    )
    parser.add_argument(
        "--jobs",
        type=int,
        default=1,
        help="Number of concurrent worker threads (default: 1)",
    )
    parser.add_argument(
        "--failure-log",
        type=Path,
        default=None,
        help="Optional JSONL path to write per-file failure diagnostics",
    )
    parser.add_argument(
        "--print-failures",
        type=int,
        default=0,
        help="Print the first N failure diagnostics to stdout (default: 0)",
    )
    parser.add_argument(
        "--hole-token",
        type=str,
        default=HOLE_TOKEN,
        help=f"Token to use for holes (default: {HOLE_TOKEN})",
    )
    parser.add_argument(
        "--parser",
        type=str,
        choices=["ast", "raw"],
        default="ast",
        help="Tactic parser backend: ast (Lean extractor) or raw (text heuristic)",
    )
    parser.add_argument(
        "--val-ratio",
        type=float,
        default=0.0,
        help="Validation split ratio in [0, 1). 0 disables split (default: 0.0)",
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=0,
        help="Random seed used for deterministic train/val split (default: 0)",
    )
    args = parser.parse_args()

    # Handle val_manifest_path default if not provided
    if args.val_manifest_path is None and args.val_ratio <= 0:
        # If no val split requested and no explicit val manifest, use default
        if DEFAULT_VAL_MANIFEST_PATH.exists():
            args.val_manifest_path = DEFAULT_VAL_MANIFEST_PATH

    configure_elan_toolchain(args.project_path)
    if not args.project_path.is_dir():
        raise FileNotFoundError(f"Project path does not exist: {args.project_path}")
    if not EXTRACT_DATA_PATH.is_file():
        raise FileNotFoundError(f"Missing ExtractData.lean at: {EXTRACT_DATA_PATH}")

    def process_manifest(manifest_path: Path, desc: str) -> tuple[list[dict[str, Any]], list[dict[str, Any]], Counter[str]]:
        """Process a single manifest and return examples, failures, and failure reasons."""
        manifest = load_manifest(manifest_path)

        if args.max_theorems is not None:
            manifest = manifest[: args.max_theorems]

        examples: list[dict[str, Any]] = []
        failures: list[dict[str, Any]] = []
        reasons: Counter[str] = Counter()
        theorems_ok = 0

        progress = tqdm(
            total=len(manifest),
            desc=desc,
            unit="theorem",
        )

        def ingest_result(
            result: tuple[int, list[dict[str, Any]] | None, dict[str, Any] | None],
        ) -> None:
            nonlocal theorems_ok
            row_idx, exs, failure_rec = result
            if exs is not None:
                theorems_ok += 1
                if args.max_examples_per_theorem is not None:
                    exs = exs[: args.max_examples_per_theorem]
                examples.extend(exs)
            elif failure_rec is not None:
                failures.append(failure_rec)
                reasons[failure_rec["reason"]] += 1

        if args.jobs == 1:
            for entry in manifest:
                ingest_result(
                    process_file(
                        entry,
                        args.project_path,
                        args.extract_timeout,
                        args.hole_token,
                        args.parser,
                    )
                )
                progress.update(1)
                if progress.n % 25 == 0:
                    progress.set_postfix(
                        theorems_ok=theorems_ok,
                        total_examples=len(examples),
                        failed=len(failures),
                    )
        else:
            with ThreadPoolExecutor(max_workers=args.jobs) as executor:
                futures = [
                    executor.submit(
                        process_file,
                        entry,
                        args.project_path,
                        args.extract_timeout,
                        args.hole_token,
                        args.parser,
                    )
                    for entry in manifest
                ]
                for future in as_completed(futures):
                    ingest_result(future.result())
                    progress.update(1)
                    if progress.n % 25 == 0:
                        progress.set_postfix(
                            theorems_ok=theorems_ok,
                            total_examples=len(examples),
                            failed=len(failures),
                        )

        progress.close()
        return examples, failures, reasons

    # Process train manifest
    print(f"[train] Loading manifest from {args.train_manifest_path}")
    train_examples, train_failures, train_reasons = process_manifest(
        args.train_manifest_path, "Processing train manifest"
    )
    print(f"[train] Processed {len(train_examples)} examples, {len(train_failures)} failures")

    # Process val manifest if provided (separate from val-ratio split)
    val_examples: list[dict[str, Any]] = []
    val_failures: list[dict[str, Any]] = []
    val_reasons: Counter[str] = Counter()
    if args.val_manifest_path is not None:
        print(f"[val] Loading manifest from {args.val_manifest_path}")
        val_examples, val_failures, val_reasons = process_manifest(
            args.val_manifest_path, "Processing val manifest"
        )
        print(f"[val] Processed {len(val_examples)} examples, {len(val_failures)} failures")

    # Combine for output
    all_examples = train_examples + val_examples
    failure_rows = train_failures + val_failures
    failure_reasons = train_reasons + val_reasons

    # Sort examples by file path and hole index for consistency
    all_examples.sort(key=lambda ex: (ex["file_path"], ex["infilling"]["hole_index"]))
    failure_rows.sort(key=lambda rec: rec["row_idx"])

    # Determine output paths
    args.output_json.parent.mkdir(parents=True, exist_ok=True)
    train_path: Path
    val_path: Path | None = None

    if args.val_manifest_path is not None and args.val_ratio <= 0:
        # Separate train/val manifests were provided - write them separately
        train_path = args.output_json.with_name(
            f"{args.output_json.stem}.train{args.output_json.suffix}"
        )
        val_path = args.output_json.with_name(
            f"{args.output_json.stem}.val{args.output_json.suffix}"
        )
        train_path.write_text(
            json.dumps(train_examples, ensure_ascii=False, indent=2), encoding="utf-8"
        )
        val_path.write_text(
            json.dumps(val_examples, ensure_ascii=False, indent=2), encoding="utf-8"
        )
    elif args.val_ratio > 0:
        # Split train examples using val_ratio
        split_train, split_val = split_examples_train_val(
            train_examples, args.val_ratio, args.seed
        )
        train_path = args.output_json.with_name(
            f"{args.output_json.stem}.train{args.output_json.suffix}"
        )
        val_path = args.output_json.with_name(
            f"{args.output_json.stem}.val{args.output_json.suffix}"
        )
        train_path.write_text(
            json.dumps(split_train, ensure_ascii=False, indent=2), encoding="utf-8"
        )
        val_path.write_text(
            json.dumps(split_val, ensure_ascii=False, indent=2), encoding="utf-8"
        )
        train_examples = split_train
        val_examples = split_val
    else:
        # No val split - write all to single file
        train_path = args.output_json
        val_examples = []
        train_path.write_text(
            json.dumps(all_examples, ensure_ascii=False, indent=2), encoding="utf-8"
        )
        val_path = None
        args.output_json.write_text(
            json.dumps(all_examples, ensure_ascii=False, indent=2), encoding="utf-8"
        )

    if args.failure_log is not None:
        args.failure_log.parent.mkdir(parents=True, exist_ok=True)
        with args.failure_log.open("w", encoding="utf-8") as f:
            for rec in failure_rows:
                f.write(json.dumps(rec, ensure_ascii=False) + "\n")

    # Count unique theorems across all examples
    total_examples = len(all_examples)
    num_theorems = len({(ex.get("file_path"), ex.get("full_name")) for ex in all_examples})

    print(f"\nSummary:")
    print(f"  Total infilling examples: {total_examples}")
    print(f"  Unique theorems: {num_theorems}")
    print(f"  Failed files: {num_failed}")

    if val_path is not None:
        print(f"  Train examples: {len(train_examples)}")
        print(f"  Val examples: {len(val_examples)}")
        print(f"  Wrote train: {train_path}")
        print(f"  Wrote val: {val_path}")
    else:
        print(f"  Wrote: {train_path}")

    if failure_reasons:
        print("\nFailure reasons:")
        for reason, count in failure_reasons.most_common():
            print(f"  - {reason}: {count}")

    if args.failure_log is not None:
        print(f"\nFailure log: {args.failure_log}")

    if args.print_failures > 0 and failure_rows:
        print(f"\nFirst {min(args.print_failures, len(failure_rows))} failures:")
        for rec in failure_rows[: args.print_failures]:
            print(
                f"  - row={rec['row_idx']} reason={rec['reason']} "
                f"uuid={rec.get('uuid')} module={rec.get('module')}"
            )


if __name__ == "__main__":
    main()
